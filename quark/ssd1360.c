byte printNumber(long long_num, byte X, byte Y){

	if ( X < 128 )
		setCursorXY(X, Y);


	byte char_buffer[10] = "";
	byte i = 0;
	byte f = 0; // number of characters

	if (long_num < 0) {

		f++;
		printChar('-', 255, 255);
		long_num = -long_num;

	}
	else if (long_num == 0) {

		f++;
		printChar('0', 255, 255);
		return f;

	}

	while (long_num > 0) {

		char_buffer[i++] = long_num % 10;
		long_num /= 10;

	}

	f += i;
	for(; i > 0; i--) {

		printChar('0'+ char_buffer[i - 1], 255, 255);

	}

	return f;

}




byte printFloatNumber(float float_num, byte prec, byte X, byte Y){

	if ( X < 128 )
		setCursorXY(X, Y);

// prec - 6 maximum

	byte num_int = 0;
	byte num_frac = 0;
	byte num_extra = 0;

	long d = float_num; // get the integer part
	float f = float_num - d; // get the fractional part


	if (d == 0 && f < 0.0){

		printChar('-', 255, 255);
		num_extra++;
		printChar('0', 255, 255);
		num_extra++;
		f *= -1;

	}
	else if (d < 0 && f < 0.0){

		num_int = printNumber(d, 255, 255); // count how many digits in integer part
		f *= -1;

	}
	else{

		num_int = printNumber(d, 255, 255); // count how many digits in integer part

	}

	// only when fractional part > 0, we show decimal point
	if (f > 0.0){

		printChar('.', 255, 255);
		num_extra++;

		long f_shift = 1;

		if (num_int + prec > 8)
			prec = 8 - num_int;

		for (byte j=0; j<prec; j++){
			f_shift *= 10;
		}

		num_frac = printNumber((long)(f*f_shift), 255, 255); // count how many digits in fractional part

	}

	return num_int + num_frac + num_extra;

}



void printBigNumber(const char *number, byte X, byte Y, byte numChar){
// big number pixels: 24 x 32

 // Y - page
	byte column = 0;
	byte count = 0;

	while(number[count] && count<numChar){


		setCursorXY(X, Y);

		for(byte i=0; i<96; i++) {

			// if character is not "0-9" or ':'
			if(number[count] < 48 || number[count] > 58)
				sendData(0);
			else
				//sendData(&bigNumbers[number[count]-48][i]);
				sendData(bigNumbers[number[count]-48][i]);


			if(column >= 23){
				column = 0;
				setCursorXY(X, ++Y);
			}
			else
				column++;

		}

		count++;

		X = X + 3;
		Y = Y - 4;


	}



}


